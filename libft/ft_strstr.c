#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int pos;
	int i;
	int len;

	pos = 0;
	i = 0;
	len = 0;
	len = ft_strlen(s2);
	if (len == 0)
		return ((char *)s1);
	while (s1[i])
	{
		while (s2[pos] == s1[i + pos])
		{
			if (pos == len - 1)
				return ((char *)s1 + i);
			pos++;
		}
		pos = 0;
		i++;
	}
	return (0);
}
